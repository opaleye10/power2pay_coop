    public function printreceipt($data)
    {
        $sth=$this->db->prepare("SELECT * FROM tbl_pos WHERE parentid=:pid AND subid=:sid AND tme=:tme AND period=:period");
        $sth->execute(array(
            ':pid'=>Session::get('parentcompanyid'),
            ':sid'=>Session::get('subsidiaryid'),
            ':period'=>Session::get('period'),
            ':tme'=>$data['id']
            ));
        return $sth->fetchAll();
    }
    

